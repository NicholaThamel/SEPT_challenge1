package com.example.model;

public class Item {

    private int id;
    private String name;
    private String desc;
    private double price;

    public Item() {

    }

    public Item(int id, String name, String desc, double price) {
        this.id = id;
        this.name = name;
        this.desc = desc;
        this.price = price;
    }


}